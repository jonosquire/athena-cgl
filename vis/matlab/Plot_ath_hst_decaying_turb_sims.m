% Plot_ath_hst.m

% General script for plotting of lots of ath_plot_hst simulations

folder = '../../../turb-tests/stampede-decay/';
files = {'MHD-N32-b128', 'visbrag-N32-b128', 'cglmhd-N32-b128-hlle', 'cglmhd-N32-b128-hlld', 'cglbrag-N32-b128-hlle',...
    'MHD-N64-b128','MHD-N64-b128-hlle','MHD-N64-b128-r2','MHD-N64-b128-vl2', 'visbrag-N64-b128', 'cglmhd-N64-b128-hlle', ...
                        'cglmhd-N64-b128-hlld', 'cglbrag-N64-b128-hlle','cglbrag-N64-b128-hlld',...
     'MHD-N128-b128','visbrag-N128-b128', 'cglmhd-N128-b128-hlle', 'cglmhd-N128-b128-hlld', 'cglbrag-N128-b128-hlle'};
legnames = {'MHD $\beta = 128,\; N_{\perp}=32$','Viscous $\beta = 128,\; N_{\perp}=32$','CGL MHD $\beta = 128,\; N_{\perp}=32$ hlle',...
                                'CGL MHD $\beta = 128,\; N_{\perp}=32$ hlld','CGL $\beta = 128,\; N_{\perp}=32$ hlle',...
    'MHD $\beta = 128,\; N_{\perp}=64$','MHD $\beta = 128,\; N_{\perp}=64$ hlle','MHD $\beta = 128,\; N_{\perp}=64$ recon 2','MHD $\beta = 128,\; N_{\perp}=64$ vl2',...
                            'Viscous $\beta = 128,\; N_{\perp}=64$','CGL MHD $\beta = 128,\; N_{\perp}=64$ hlle',...
                                'CGL MHD $\beta = 128,\; N_{\perp}=64$ hlld','CGL $\beta = 128,\; N_{\perp}=64$ hlle','CGL $\beta = 128,\; N_{\perp}=64$ hlld',...
    'MHD $\beta = 128,\; N_{\perp}=128$','Viscous $\beta = 128,\; N_{\perp}=128$','CGL MHD $\beta = 128,\; N_{\perp}=128$',...
                                'CGL MHD $\beta = 128,\; N_{\perp}=128$ hlld','CGL $\beta = 128,\; N_{\perp}=128$ hlle'};
linesty = {':',':',':^',':',':',...
    '--','--^','--*','--s','--','--^','--','--','--^',...
    '-','-','-^','-','-'};
col = [1 3 2 2 5 ...
    1 1 1 1 3 2 2 5 5 ...
    1 3 2 2 5];


origCO=get(gcf,'DefaultAxesColorOrder');
origCO = [[0.5 0.5 0.5];origCO];
stride = 50;
% figure
for kk=1:length(files)
    fname = [folder files{kk} '/Turb'];
    fulldata = importdata([fname '.hst']);
    dat = fulldata.data;
    m2 = size(dat,2);
    nums=[8 9 m2-1 m2];
    semilogy(dat(1:stride:end,1),sum(dat(1:stride:end,nums),2)/(2*dat(1,m2-2)),...
        linesty{kk},'Color',origCO(col(kk),:),'Markersize',3,'Linewidth',1)
    drawnow
    hold on
end

legend(legnames,'interpreter','latex') 
xlabel('$t$','interpreter','latex') 
ylabel('$E_{\mathrm{turb}}$','interpreter','latex') 
xlim([0 260])
ylim([5e-5 1e-2])